package DataBaseHandle;


import java.util.ArrayList;



public class Pair {
	String key;
	String value;
	
	public Pair(String key, String value) {
		super();
		this.key = key;
		this.value = value;
	}

    public void setKey(String key) {
        this.key = key;
    }

    public void setValue(String value) {
        this.value = value;
    }
	public Pair() {
		
	}

	
}